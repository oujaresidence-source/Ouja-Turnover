HOOK_RULES = """قواعد الهوك (أول ٣ ثواني تقرر التوزيع — إكمال المشاهدة أهم إشارة عند تيك توك،
وعدد المتابعين مو عامل ترتيب، فأي فيديو ممتاز يوصل مليون بذاته):
١. صمّم الهوك المرئي أولاً ثم الكلام: وش اللقطة/النص اللي يجمّد الإبهام؟ بعدها اكتب الجملة المنطوقة.
٢. أول جملة تُنطق = cold open وسط الحدث. لا مقدمات، لا "هلا وغلا"، لا تعريف بالنفس. الهوك كله ≤ ٣ ثواني.
٣. النص على الشاشة إجباري (٣٠٪+ يشاهدون بدون صوت): يشتغل مع المنطوق ما يكرره — العنوان يفتح فجوة،
   والسطر الثاني يرفع الرهان أو يحدد «لمن هذا». والكلمة المنطوقة تُفهرس للبحث فقُل اسم الرياض/نوع الشقة بصوت.
٤. لغة بسيطة مستوى طفل بخامس ابتدائي. جُمل قصيرة. مصطلح واحد صعب = سكرول.
٥. أقوى صيغ الهوك المثبتة ٢٠٢٦ — اختر منها:
   • نداء الهوية (الأقوى): «لو عندك شقة في الرياض...» — يخاطب الشريحة مباشرة.
   • الفجوة المفتوحة (open loop): سؤال/موقف حقيقي يفتح فضول لازم يكمل عشان يعرف الجواب.
   • الاعتراف الصادق (confession): «أكبر غلط سوّيناه أول سنة...» — يركب على براند الشفافية.
   • الرأي المخالف المؤسَّس (contrarian): موقف عكس السائد لكن مبني على خبرة حقيقية — مو استفزاز فاضي.
   • الدليل أولاً (proof-first): ابدأ برقم/نتيجة حقيقية من عوجا (إشغال، تقييم، مدة رد) في أول ثانية.
   • سؤال ألم محدّد: سؤال يلمس وجع محدّد للمالك/الضيف (يرفع الإكمال ~٢٣٪).
٦. التحديد يغلب التعميم: «ضيف حجز ٣ ليالي وطلب شي ما توقعناه» أقوى من «قصة غريبة». رقم حقيقي + تفصيلة
   حسية واحدة = مصداقية فورية.
٧. الصدق شرط: الهوك يوعد بس باللي يسلّمه الفيديو. جمهور فيصل يثق فيه — هوك مبالغ يكسب فيديو ويخسر البراند.
٨. ممنوع نهائياً (كليشيهات محروقة + تُخفّض التوزيع): «لن تصدق»، «انتظر للنهاية»، «قصة صادمة»،
   «هل تعلم؟»، «POV:» المستهلك، أي هوك يبدأ بتعريف النفس أو تحية عامة، وأي rage-bait/استفزاز غاضب
   (يعطي قفزة وقتية ويضرّ البراند طويل المدى ويُكبت بإشارة «غير مهتم»). والأرقام الوحيدة المسموحة أرقام عوجا الحقيقية."""

